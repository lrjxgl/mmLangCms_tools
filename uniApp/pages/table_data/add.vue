<template>
	<view>
	<div class="tabs-border">
			<div @click="gourl('index')" class="item">列表</div>
			<div @click="gourl('add')" class="item active">添加</div>
		</div>
<form @submit="submit">
<input type="text" name="id" class="none" v-model="data.id" >
 <table class="table-add">  <tr>
						<td>tableid：</td>		
						<td><input class="input-text" type="text" name="tableid"   v-model="data.tableid" ></td>
						</tr>
  <tr>
						<td>分类：</td>		
						<td><input class="input-text" type="text" name="catid"   v-model="data.catid" ></td>
						</tr>
  <tr>
						<td>objectid：</td>		
						<td><input class="input-text" type="text" name="objectid"   v-model="data.objectid" ></td>
						</tr>
  <tr>
						<td>userid：</td>		
						<td><input class="input-text" type="text" name="userid"   v-model="data.userid" ></td>
						</tr>
  <tr>
						<td>createtime：</td>		
						<td><input class="input-text" type="text" name="createtime"   v-model="data.createtime" ></td>
						</tr>
  <tr>
						<td>status：</td>		
						<td>
						<input type="text" class="none" name="status" v-model="data.status" />
						<radio-group>
							<radio type="text" class="mgr-5" checked="data.status==1"  value="1" > 上线</radio>
							<radio type="text" checked="data.status!=1"    value="2" > 下线</radio>
						</radio-group>	
						</td>
						</tr>
  <tr>
						<td>comment_num：</td>		
						<td><input class="input-text" type="text" name="comment_num"   v-model="data.comment_num" ></td>
						</tr>
  <tr>
						<td>view_num：</td>		
						<td><input class="input-text" type="text" name="view_num"   v-model="data.view_num" ></td>
						</tr>
  <tr>
						<td>content：</td>		
						<td><input class="input-text" type="text" name="content"   v-model="data.content" ></td>
						</tr>
</table> <button form-type="submit" class="btn-row-submit">保存</button> 
</form>
	</view>
</template>
<script>
	import upimgBox from "../../components/upimgbox.vue";
	import skyUpimg from "../../components/skyupimg.vue";
	export default {
		components: {
			upimgBox,
			skyUpimg
		},
		data: function() {
			return {
				'.id.': 0,
				pageLoad: false,
				data: {},
				imgsList: []
			}
		},
		onLoad: function(ops) {
			if (ops.'.id.' != undefined) {
				this.'.id.' = ops.id;
			}
			console.log("onLoad")
			this.getPage();
		},
		methods: {
			gourl: function(url) {
				uni.navigateTo({
					url: url
				})
			},
			setImgs: function(e) {
				this.data.imgsdata = e;
			},
			setImgurl: function(e) {
				this.data.imgurl = e;
			},
			getPage: function() {
				var that = this;
				that.app.get({
					url: that.app.apiHost + "/admin/table_data/add",
					data: {
						'.id.': this.'.id.'
					},
					success: function(res) {
						if (Object.keys(res.data).length > 0) {
							that.data = res.data;
							that.imgsList = res.imgsdata;
						} else {
							that.data = {
								
id: "0",
tableid: "0",
catid: "0",
objectid: "0",
userid: "0",
createtime: "",
status: "0",
comment_num: "0",
view_num: "0",
content: "",

							}
						}

						that.pageLoad = true;
					}
				})
			},
			submit: function(e) {
				var that = this;
				that.app.post({
					url: that.app.apiHost + "/admin/table_data/save",
					data: e.detail.value,
					success: function(res) {
						uni.showToast({
							title: res.message
						})
					}
				})

			}
		}
	}
</script>

